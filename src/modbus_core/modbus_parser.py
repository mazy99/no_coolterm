def parse_module_17_registers(registers: list, start_address: int = 0) -> list:
    """
    Преобразует список полученных регистров в список словарей с адресом, названием, значением и масштабом
    
    Args:
        registers: список чисел (значений регистров)
        start_address: начальный адрес, с которого производилось чтение
    
    Returns:
        list of dict: [{"address": "0x0000", "name": "...", "raw_value": 123, "scaled_value": 12.3, "scale": 10}, ...]
    """
    result = []
    
    for i, raw_value in enumerate(registers):
        address = start_address + i
        name = REGISTER_NAMES.get(address, f"Неизвестный регистр")
        scale = REGISTER_SCALES.get(address, 1)
        
        # Применяем масштаб
        if scale == 1:
            scaled_value = raw_value
            formatted_scaled = f"{scaled_value}"
        else:
            scaled_value = raw_value / scale
            # Форматируем с нужным количеством знаков
            if scale >= 100:
                formatted_scaled = f"{scaled_value:.3f}"
            else:
                formatted_scaled = f"{scaled_value:.1f}"
        
        # Обработка знаковых значений для температуры
        if address == 0x0005 or address == 0x0014:
            if raw_value > 32767:
                raw_value = raw_value - 65536
            scaled_value = raw_value / scale
            if scale >= 100:
                formatted_scaled = f"{scaled_value:.3f}"
            else:
                formatted_scaled = f"{scaled_value:.1f}"
        
        result.append({
            "address": f"0x{address:04X}",
            "name": name,
            "raw_value": raw_value,
            "scaled_value": scaled_value,
            "formatted_value": formatted_scaled,
            "scale": scale
        })
    
    return result